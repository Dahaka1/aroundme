import random

subjects = [
    "Бабушка с баяном", "Левый дворник", "Анонимный художник", "Комитет по веселью",
    "Случайный прохожий", "Ратник света", "Хомячки сопротивления", "Инфлюенсер с района",
    "Кот в наушниках", "Безработный диджей", "Голубь-проповедник", "IT-стажёр", "Загадочный дед"
]

verbs = [
    "призывает к", "срывает", "неожиданно устраивает", "запрещает", "медитирует на",
    "стримит", "раскладывает карты Таро на", "под шумок запускает", "манифестирует",
    "делает вид что", "вызывает духов для", "рекламирует", "взламывает"
]


objects = [
    "чистку ауры", "тайный рэп-баттл", "битву кастрюль", "вечер криков в подушку",
    "турнир по ничегонеделанию", "выставку левого искусства", "обнимашки с Wi-Fi",
    "бесплатную раздачу советов", "поедание сосисок с философским уклоном",
    "ритуал приманивания дождя", "лекцию по выживанию в офисе", "тихий протест против всего",
    "сеанс молчаливого стендапа", "спонтанную хоровую драку"
]

adjectives = [
    "Гигантский", "Безумный", "Скрытный", "Летающий", "Пьяный", "Токсичный", "Магический",
    "Злобный", "Хаотичный", "Космический", "Неожиданный", "Криповый", "Пародийный"
]

nouns = [
    "Кот", "Кролик", "НЛО", "Джедай", "Пельмень", "Додо", "Тролль", "Пингвин", "Пельмень",
    "Чебурек", "Банан", "Чайник", "Мем", "Зомби", "Гамбургер", "Киборг"
]

events = [
    "наступление", "восстание", "парад", "концерт", "захват", "эпидемия", "революция",
    "эксперимент", "погром", "фестиваль", "соревнование", "экскурсия", "марафон"
]


def generate_sentence():
    return f"{random.choice(subjects)} {random.choice(verbs)} {random.choice(objects)}."


def generate_name():
    adj = random.choice(adjectives)
    noun = random.choice(nouns)
    event = random.choice(events)
    return f"{adj} {noun} и {event}"
